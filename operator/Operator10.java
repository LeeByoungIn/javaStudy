package org.java.operator;

import java.util.Scanner;

public class Operator10 {
	public static void main(String[] args) {
		System.out.println("단항 연산자");
		
		for(int i =0; i < 10; i++) {
			System.out.println(i);
		}
		System.out.println();
		
		for(int i =0; i < 10; ++i) {
			System.out.println(i);
		}
		System.out.println();
	}
}
